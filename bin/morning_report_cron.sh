#!/bin/bash
#
# Morning Report for Cron - Uses text report only (no HTML, no AI)
#
# Generates notification from cached health data
# Uses existing enhanced text report generator
#
# Designed to run as a cron job at 0900 daily
#

set -e

# Get the directory of this script
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PROJECT_ROOT="$(cd "$SCRIPT_DIR/.." && pwd)"

# Log file
LOG_FILE="$PROJECT_ROOT/data/morning_report.log"
echo "=== Morning Report (Cron): $(date) ===" >> "$LOG_FILE"

cd "$PROJECT_ROOT"

# Use venv Python if available
if [ -f "$PROJECT_ROOT/venv/bin/python3" ]; then
    PYTHON="$PROJECT_ROOT/venv/bin/python3"
else
    PYTHON="python3"
fi

# Fetch weather
WEATHER=$("$PYTHON" src/get_weather.py 2>> "$LOG_FILE") || WEATHER="Weather unavailable"

# Generate text report
REPORT=$("$PYTHON" src/generate_enhanced_report.py "$WEATHER" 2>> "$LOG_FILE") || {
    echo "Report generation failed" >> "$LOG_FILE"
    termux-notification --title "Morning Report Error" --content "Failed to generate report" --channel morning-report
    exit 1
}

# Extract key metrics for notification (aim for ~200-250 chars max)
SLEEP=$(echo "$REPORT" | grep "Sleep:" | head -1 | sed 's/^[ \t]*Sleep: //')
RHR=$(echo "$REPORT" | grep "RHR:" | head -1 | sed 's/^[ \t]*RHR: //')
WORKOUT=$(echo "$REPORT" | grep -E "Today's Workout:" | head -1 | sed 's/^[ \t]*Today'\''s Workout: //' || echo "Rest day")
if [ -z "$WORKOUT" ]; then
    WORKOUT=$(echo "$REPORT" | grep "No workout scheduled" | head -1)
    if [ ! -z "$WORKOUT" ]; then
        WORKOUT="Rest day"
    fi
fi

# Build concise 3-line summary
LINE1="Sleep: ${SLEEP:-N/A}"
LINE2="RHR: ${RHR:-N/A}"
LINE3="Today: ${WORKOUT:-Check details}"

SUMMARY="$LINE1
$LINE2
$LINE3"

# Fallback if nothing matched
if [ -z "$SLEEP" ] && [ -z "$RHR" ]; then
    SUMMARY="Morning Report Ready
Tap 'View Details' for full report"
fi

# Generate simple HTML report (no AI - just data visualization)
echo "Generating HTML report..." >> "$LOG_FILE"
DETAILED_REPORT_HTML="$PROJECT_ROOT/data/morning_report_detailed.html"

# Convert text report to simple HTML
REPORT_HTML=$(echo "$REPORT" | sed 's/&/\&amp;/g; s/</\&lt;/g; s/>/\&gt;/g')
WEATHER_HTML=$(echo "$WEATHER" | sed 's/&/\&amp;/g; s/</\&lt;/g; s/>/\&gt;/g')

cat > "$DETAILED_REPORT_HTML" << 'EOF'
<!DOCTYPE html>
<html>
<head>
    <meta charset="UTF-8">
    <meta name="viewport" content="width=device-width, initial-scale=1.0">
    <title>Morning Training Report</title>
    <style>
        body {
            font-family: -apple-system, BlinkMacSystemFont, 'Segoe UI', Roboto, sans-serif;
            max-width: 800px;
            margin: 0 auto;
            padding: 20px;
            background: #f5f5f5;
            line-height: 1.6;
        }
        .container {
            background: white;
            border-radius: 12px;
            padding: 24px;
            box-shadow: 0 2px 8px rgba(0,0,0,0.1);
        }
        h1 {
            color: #2c3e50;
            border-bottom: 3px solid #3498db;
            padding-bottom: 12px;
            margin-top: 0;
        }
        h2 {
            color: #34495e;
            margin-top: 24px;
            margin-bottom: 12px;
            font-size: 1.3em;
        }
        .section {
            margin: 20px 0;
            padding: 16px;
            background: #f8f9fa;
            border-left: 4px solid #3498db;
            border-radius: 4px;
        }
        .timestamp {
            color: #7f8c8d;
            font-size: 0.95em;
            margin-bottom: 20px;
        }
        pre {
            background: #2c3e50;
            color: #ecf0f1;
            padding: 16px;
            border-radius: 6px;
            overflow-x: auto;
            white-space: pre-wrap;
            word-wrap: break-word;
            font-size: 0.9em;
            line-height: 1.5;
        }
        .footer {
            margin-top: 32px;
            padding-top: 16px;
            border-top: 1px solid #ddd;
            color: #7f8c8d;
            font-size: 0.85em;
            text-align: center;
        }
        .metric {
            display: inline-block;
            margin: 8px 12px 8px 0;
            padding: 8px 12px;
            background: white;
            border-radius: 6px;
            border: 1px solid #e0e0e0;
        }
    </style>
</head>
<body>
    <div class="container">
        <h1>🏃 Morning Training Report</h1>
        <p class="timestamp">EOF
echo "        <p class=\"timestamp\">$(date '+%A, %B %d, %Y at %I:%M %p')</p>" >> "$DETAILED_REPORT_HTML"
cat >> "$DETAILED_REPORT_HTML" << EOF

        <div class="section">
            <h2>📊 Health & Training Summary</h2>
            <pre>$REPORT_HTML</pre>
        </div>

        <div class="section">
            <h2>🌤️ Weather Conditions</h2>
            <pre>$WEATHER_HTML</pre>
        </div>

        <div class="footer">
            Generated by Running Coach System<br>
            Logs: data/morning_report.log
        </div>
    </div>
</body>
</html>
EOF

# Copy HTML to Downloads folder
DOWNLOADS_HTML="$HOME/storage/downloads/morning_report.html"
cp "$DETAILED_REPORT_HTML" "$DOWNLOADS_HTML" 2>> "$LOG_FILE"
echo "HTML report saved to Downloads" >> "$LOG_FILE"

# Send notification with button to view HTML
echo "Sending notification..." >> "$LOG_FILE"

termux-notification \
    --title "🏃 Morning Report" \
    --content "$SUMMARY" \
    --channel morning-report \
    --priority high \
    --sound \
    --button1 "View Details" \
    --button1-action "termux-share $DOWNLOADS_HTML"

echo "Sent: $SUMMARY" >> "$LOG_FILE"
echo "" >> "$LOG_FILE"
